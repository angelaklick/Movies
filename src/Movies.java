
public class Movies {
	
	private String title;
	private String category;

	public Movies(String t, String c){
		
		title = t;
		category = c;
	}
	
	public void setTitle(String t){
		
		title = t;
		
	}
	public void setCategory(String c){
		
		category = c;
		
	}
	public String getTitle(){
		
		return title;
		
	}
	public String getCategory(){
		
		return category;
		
	}
	
	
	
	
	
	
}
